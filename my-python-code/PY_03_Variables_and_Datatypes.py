# Integer variables
num1 = 5646
num2 = 85499

# Float variable
float_num = 8.55

# Complex number
complex_num = complex(6, 5)

# String variable
name = "Saksham"

# Boolean variable
is_active = True

# NoneType variable
empty = None

# Print variables
print(num1)
print(num2)
print(float_num)
print(complex_num)
print(name)
print(is_active)
print(empty)

# addition of float and integer (results in float)
print(float_num + num2)

# Print variable types
print("The type of name is", type(name))
print("The type of num1 is", type(num1))

# List with mixed data types
list = [8, 9, 6, [-4, 5], ["apple", "saksham"]]
print(list)

# Tuple with nested tuples
tuple = (("man", "women"), ("boy", "girl"))
print(tuple)

# Dictionary with key-value pairs
dict = {"name": "saksham", "age": 19, "canVote": True}
print(dict)