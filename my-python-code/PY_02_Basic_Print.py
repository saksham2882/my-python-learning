""""
    this is multi line comment
    this is multi line comment 
"""

# Print a message with a newline using escape sequence \n
print("He is a good boy\nand his teacher is also a good man")

# Use \" to include double quotes in a string
print("He is a \"good boy\"\nand his teacher is also a good man")

# Use \' to include single quotes in a string
print('\'He is\' a \"good boy\"\nand his teacher is also a good man')

# Use sep to customize separator and end to customize line ending
print("Hey", 28, 45, sep="-", end="009\n")
